//use for loop to calculate the sum of first 100 natural number

class Main {
    public static void main(String args[])
    {
        int sum=0;
        for(int i=1;i<=5;i++)
        {
            sum=sum+i;
            
        }
        System.out.println("sum of 1 to 5 natural number:" +sum);
    }
}